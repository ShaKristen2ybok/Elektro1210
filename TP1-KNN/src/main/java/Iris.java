public class Iris {
    double sepalLength;
    double sepalWidth;
    double petalLength;
    double petalWidth;
    String type;

    public Iris(double sL, double  sW, double  pL, double  pW, String type){
        this.sepalLength = sL;
        this.sepalWidth = sW;
        this.petalLength = pL;
        this.petalWidth = pW;
        this.type = type;
    }
}
