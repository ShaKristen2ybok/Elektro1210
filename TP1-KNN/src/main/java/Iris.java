public class Iris {
    float sepalLength;
    float sepalWidth;
    float petalLength;
    float petalWidth;
    String type;

    public Iris(float sL, float  sW, float  pL, float  pW, String type){
        this.sepalLength = sL;
        this.sepalWidth = sW;
        this.petalLength = pL;
        this.petalWidth = pW;
        this.type = type;
    }
}
